const SidebarMenuButton = React.forwardRef<
    HTMLButtonElement,
    React.ComponentProps<"button"> & {
        asChild?: boolean
        isActive?: boolean
        tooltip?: string | React.ComponentProps<typeof TooltipContent>
        variant?: "default" | "outline"
        size?: "default" | "sm" | "lg"
    }
>(
    (
        {
            asChild = false,
            isActive = false,
            variant = "default",
            size = "default",
            tooltip,
            className,
            ...props
        },
        ref
    ) => {
        const Comp = asChild ? React.Fragment : "button"
        const { isMobile, state } = useSidebar()

        // Simple button styled manually since we might not have all variants
        // Using simple styling here to match Shadcn's intent
        const buttonClass = cn(
            "peer/menu-button flex w-full items-center gap-2 overflow-hidden rounded-md p-2 text-left text-sm outline-none ring-sidebar-ring transition-[width,height,padding] hover:bg-sidebar-accent hover:text-sidebar-accent-foreground focus-visible:ring-2 active:bg-sidebar-accent active:text-sidebar-accent-foreground disabled:pointer-events-none disabled:opacity-50 group-has-[[data-sidebar=menu-action]]/menu-item:pr-8 aria-disabled:pointer-events-none aria-disabled:opacity-50 data-[active=true]:bg-sidebar-accent data-[active=true]:font-medium data-[active=true]:text-sidebar-accent-foreground data-[state=open]:hover:bg-sidebar-accent data-[state=open]:hover:text-sidebar-accent-foreground group-data-[collapsible=icon]:!size-8 group-data-[collapsible=icon]:!p-2 [&>span:last-child]:truncate [&>svg]:size-4 [&>svg]:shrink-0",
            className
        )

        if (asChild) {
            // NOTE: This is a simplified version for this environment, wrapping child content might be complex without Slot
            return (
                <button ref={ref} className={buttonClass} {...props} />
            )
        }

        return (
            <button ref={ref} className={buttonClass} data-active={isActive} {...props} />
        )
    }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

